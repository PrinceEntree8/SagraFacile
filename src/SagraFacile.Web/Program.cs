using System.Text;
using Microsoft.AspNetCore.Authentication;
using Microsoft.AspNetCore.Authentication.JwtBearer;
using Microsoft.AspNetCore.Localization;
using Microsoft.EntityFrameworkCore;
using Microsoft.IdentityModel.Tokens;
using SagraFacile.Application;
using SagraFacile.Application.Interfaces;
using SagraFacile.Infrastructure;
using SagraFacile.Infrastructure.Data;
using SagraFacile.Infrastructure.Identity;
using SagraFacile.Web.Components;
using SagraFacile.Web.Hubs;

var builder = WebApplication.CreateBuilder(args);

builder.Services.AddRazorComponents()
    .AddInteractiveServerComponents();

builder.Services.AddLocalization();

builder.Services.AddSignalR();
builder.Services.AddScoped<IReservationNotifier, SignalRReservationNotifier>();

var connectionString = builder.Configuration.GetConnectionString("DefaultConnection")
    ?? "Host=localhost;Port=5432;Database=sagrafacile;Username=postgres;Password=postgres";

builder.Services.AddInfrastructureServices(connectionString);
builder.Services.AddApplicationServices();

builder.Services.ConfigureApplicationCookie(options =>
{
    options.LoginPath = "/login";
    options.LogoutPath = "/logout";
    options.AccessDeniedPath = "/access-denied";
    options.ExpireTimeSpan = TimeSpan.FromHours(8);
    options.SlidingExpiration = true;
});

var jwtKey = builder.Configuration["Jwt:Key"];
var jwtKeyAutoGenerated = false;
if (string.IsNullOrWhiteSpace(jwtKey))
{
    // No key configured: auto-generate a random one so the app starts without manual setup.
    // Useful for LAN/offline deployments where a stable key is not critical.
    // WARNING: every restart invalidates all existing sessions.
    // Set Jwt:Key in configuration or environment variables for stable sessions.
    jwtKey = Convert.ToBase64String(System.Security.Cryptography.RandomNumberGenerator.GetBytes(32));
    jwtKeyAutoGenerated = true;
}

builder.Services.AddAuthentication()
    .AddJwtBearer(JwtBearerDefaults.AuthenticationScheme, options =>
    {
        options.TokenValidationParameters = new TokenValidationParameters
        {
            ValidateIssuer = true,
            ValidateAudience = true,
            ValidateLifetime = true,
            ValidateIssuerSigningKey = true,
            ValidIssuer = builder.Configuration["Jwt:Issuer"] ?? "SagraFacile",
            ValidAudience = builder.Configuration["Jwt:Audience"] ?? "SagraFacile",
            IssuerSigningKey = new SymmetricSecurityKey(Encoding.UTF8.GetBytes(jwtKey))
        };
    });

builder.Services.AddAuthorization(options =>
{
    options.AddPolicy("AdminOnly", policy => policy.RequireRole("Admin"));
    options.AddPolicy("AdminOrSupervisore", policy => policy.RequireRole("Admin", "Supervisore"));
    options.AddPolicy("Cassiere", policy => policy.RequireRole("Admin", "Supervisore", "Cassiere"));
    options.AddPolicy("Cucina", policy => policy.RequireRole("Admin", "Supervisore", "Cucina"));
});

builder.Services.AddCascadingAuthenticationState();

builder.Services.AddControllers();

var app = builder.Build();

if (jwtKeyAutoGenerated)
{
    var startupLogger = app.Services.GetRequiredService<ILogger<Program>>();
    startupLogger.LogWarning(
        "Jwt:Key is not configured. A random key was auto-generated for this session. " +
        "All authentication tokens will be invalidated on every restart. " +
        "Set Jwt:Key in configuration or the Jwt__Key environment variable for stable sessions.");
}

using (var scope = app.Services.CreateScope())
{
    var db = scope.ServiceProvider.GetRequiredService<ApplicationDbContext>();
    db.Database.Migrate();
    await DataSeeder.SeedAsync(scope.ServiceProvider);
}

// AllowHttp=true disables HTTPS redirect and HSTS for HTTP-only LAN/offline deployments.
var allowHttp = app.Configuration.GetValue<bool>("AllowHttp");

if (!app.Environment.IsDevelopment())
{
    app.UseExceptionHandler("/Error", createScopeForErrors: true);
    if (!allowHttp)
        app.UseHsts();
}
app.UseStatusCodePagesWithReExecute("/not-found", createScopeForStatusCodePages: true);
if (!allowHttp)
    app.UseHttpsRedirection();

app.UseAuthentication();
app.UseAuthorization();

var supportedCultures = new[] { "it", "en" };
app.UseRequestLocalization(new RequestLocalizationOptions
{
    DefaultRequestCulture = new RequestCulture("it"),
    SupportedCultures = supportedCultures.Select(c => new System.Globalization.CultureInfo(c)).ToList(),
    SupportedUICultures = supportedCultures.Select(c => new System.Globalization.CultureInfo(c)).ToList(),
    ApplyCurrentCultureToResponseHeaders = true,
    FallBackToParentCultures = true,
    FallBackToParentUICultures = true,
    RequestCultureProviders = new List<IRequestCultureProvider>
    {
        new AcceptLanguageHeaderRequestCultureProvider()
    }
});

app.UseAntiforgery();

app.MapStaticAssets();
app.MapControllers();
app.MapRazorComponents<App>()
    .AddInteractiveServerRenderMode();

app.MapHub<ReservationHub>("/hubs/reservations");

app.MapPost("/logout", async (HttpContext context) =>
{
    await context.SignOutAsync();
    return Results.Redirect("/login");
});

app.Run();
